a = range(80, 100)
b = range(79, 79)
c = range(60, 69)
d = range(50, 59)
e = range(0, 49)

nilai = [a, b, c, d, e]
while True:
    if nilai == a:
        print('Nilai : A')
    elif nilai == b:
        print('Nilai : B')
    elif nilai == c:
        print('Nilai : C')
    elif nilai == d:
        print('Nilai : D')
    else:
        print('Nilai : E')
    break